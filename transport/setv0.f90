!----------------------------------------------------------------------
      SUBROUTINE setv0( n, a )
!----------------------------------------------------------------------

      IMPLICIT NONE

      INTEGER :: n, i

      COMPLEX*16 :: a(n)

      DO i = 1, n
         a(i) = ( 0.d0, 0.d0 )
      END DO

      RETURN
      END

!----------------------------------------------------------------------
      SUBROUTINE setd0( n, a )
!----------------------------------------------------------------------

      IMPLICIT NONE
 
      INTEGER :: n, i

      REAL*8 :: a(n)

      DO i = 1, n
         a(i) = 0.d0
      END DO

      RETURN
      END

!----------------------------------------------------------------------
      SUBROUTINE seti0( n, a )
!----------------------------------------------------------------------

      IMPLICIT NONE

      INTEGER :: i, n

      INTEGER :: a(n)

      DO i = 1, n
         a(i) = 0
      END DO

      RETURN
      END
